#include <iostream>

using namespace std;

int main()
{
   int x = 50;
   
   cout << "x: " << x << '\n';
   ++x;
   cout << "x: " << x << '\n';
   cout << "++x: " << (++x) << '\n';
   cout << "x: " << x << '\n';
   cout << "x++: " << (x++) << '\n';
   cout << "x: " << x << '\n';
   
   
    return 0;
}
